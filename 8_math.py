x = min(5, 10, 25)
y = max(5, 10, 25)

print(x, y)

x = abs(-7.325)  # abs()함수: 지정된 숫자의 절대값(양수)
print(x)

# pow(power): 4, 3 4 * 4 * 4
e = pow(4, 3)
print(e)

import math  # import math: 수학함수 사용

x = math.sqrt(64)
print(x)  # 8

# 올림(무조건 올림)과 내림(무조건 내림)
x = math.ceil(1.4)  # 2
y = math.floor(1.4)  # 1
print(x)
print(y)

t = math.pi
print(t)
